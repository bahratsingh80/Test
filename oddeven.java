public class oddeven{
    public static void main(String[] args){
        System.out.println("Even and odd numbers from 1 to 100:");
        for (int i = 1; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " is even");
            }
            else{
                System.out.println(i + "is odd");
            }
        }
    }
    }
